<Text color="#8E8151" size="24" align="left" width="100">
1.普通鸡：幺鸡，叫牌时每张计1分；未叫牌时，打出或碰杠的幺鸡需赔付叫牌玩家。（责任鸡作为持有者的普通鸡结算分数）

2.冲锋鸡：在起牌后，所有玩家打出的第一张幺鸡为冲锋鸡，计3分。

