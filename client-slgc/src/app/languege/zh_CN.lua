8.天胡、地胡：庄家起手胡牌称为天胡，闲家摸牌前接炮或起手自摸胡牌称为地胡，无豆平胡可胡，计10分。

9.两房：在贵阳捉鸡规则基础上去掉万，只有“条”和“筒”共72张牌，胡牌更迅速，紧张刺激！！！

10.包鸡、包豆：有人胡牌后全桌查叫，未叫牌玩家亮出过的（包括打出、碰、杠）鸡和豆，需要按照收分方式反赔给叫牌玩家，应该收多少就赔多少。特殊的是，点热炮的那个豆不参与包豆。

11.黄庄查叫：所有牌都摸完之后仍然无人胡牌，则本局记为黄庄。黄庄时所有鸡豆全部作废，所有未叫牌玩家需要按照最大牌型分赔付所有叫牌玩家。
