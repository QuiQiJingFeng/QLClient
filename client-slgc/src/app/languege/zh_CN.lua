4.翻牌鸡：玩家胡牌后，翻开第1张未摸的牌，根据这张牌的数值，顺位+1的牌为鸡，计1分。

5.本鸡：翻牌鸡翻开的那张牌也是鸡，计1分。

6.摇摆鸡：翻牌的顺位+1和-1的两张牌都为鸡。

7.乌骨鸡：默认八筒为固定鸡，称为乌骨鸡，相关规则与幺鸡相同。

8.满堂鸡：所有打出的鸡和手中的鸡都参与最终结算。

9.星期鸡：当天的星期数（1至7）对应数字的万条筒（1至7）的牌也为鸡。

10.金鸡：翻牌翻到固定鸡时，该固定鸡记为金鸡，分值翻倍。
