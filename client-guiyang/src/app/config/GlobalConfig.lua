ns.GlobalConfig.getEncryptConnection = function()
    if game.plugin.Runtime.isEnabled() == false then
        -- 模拟器自己设置吧
		return true;
	end
    if not ns.GlobalConfig.ENCRYPT_CONNECTION then return false end
    local config = ns.GlobalConfig.getConfig();
    -- 如果不开则配1000.1000.1000.1000
    -- 如果全包开则配0.0.0.0
    -- 如果按版本开则配对应版本
    local ev = config.ENCRYPT_VERSION

    local Version = require "app.kod.util.Version"
	local currentVersion = Version.new(game.plugin.Runtime.getBuildVersion())
	local supportVersion = Version.new(ev)
	return currentVersion:compare(supportVersion) >= 0;
end

