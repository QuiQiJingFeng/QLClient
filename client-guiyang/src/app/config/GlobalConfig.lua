ns.GlobalConfig.getConfigArea = function(areaId)
    for i,v in ipairs( ns.GlobalConfig.AREAS) do
        if areaId == v.id then
            return v;
        end
    end
    return nil;
end

ns.GlobalConfig.getConfig = function()
    local channel = getChannel()
    local config = ns.GlobalConfig.ChannelConfig[channel]
    Macro.assetTrue(config == nil, tostring(channel))
    return config
end

ns.GlobalConfig.getDefaultIcon = function()
    if device.platform  == "android" then
        return "icon"
    elseif device.platform  == "ios" then
        local Version = require "app.kod.util.Version"
        local currentVersion = Version.new(game.plugin.Runtime.getBuildVersion())
        local supportVersion = Version.new("4.3.0.0")
        if currentVersion:compare(supportVersion) >= 0 then
            return "AppIcon60x60.png"
        else
            return "Icon-144.png"
        end
    end

    return ""
end

